public class multicon {
    public static void main(String[] args) {
        String city ="jaipur";

        if (city=="Delhi"){
            System.out.println("Student belongs to Delhi");
        }
        else if (city=="jaipur"){
            System.out.println("Student belongs to Jaipur");
        }
        else if (city=="Delhi"){
            System.out.println("Student belongs to Bikaner");
        }
        else{
            System.out.println("City is invalid");
        }
    }
    
}
