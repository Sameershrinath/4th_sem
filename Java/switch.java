public class Switch {
    public static void main (String[] args){
        int a = 3;
        switch(a){
            case 1:
                System.out.println("The number is 0");
                break;
            case 2:
                System.out.println("The number is 1");
                break;
            case 3:
                System.out.println("The number is 3");
                break;
            case 4:
                System.out.println("The number is 4");
                break;
            default :
                System.out.println("Not found !");
        }
    }
    
}
